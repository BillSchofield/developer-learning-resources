package net.billschofield.oop_examples.refactoring.parallel.game_math.exercise;

public class Vector2 {
	private final double x;
	private final double y;

	public Vector2(double x, double y) {
		this.x = x;
		this.y = y;
	}
	
	public double getX() {
		return x;
	}

	public double getY() {
		return y;
	}
}
